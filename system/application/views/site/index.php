<h1><a href="#">Добавлен раздел "Настройки таблицы"</a></h1>
Категория: Новости
</div>

<p> На данный момент система anbase адаптирована под агентства Санкт-Петербурга, <br/>
В первую очередь это отражается на том, что агентства из Санкт-Петербурга могут указывать "Район", "Метро" для каждой заявки.<br/>
Для того, чтобы и другие агентства могли спокойно работать пока мы не добавим карты других городов, можно через "настройки таблицы" <br/>
 убрать такие колонки как "метро","регион".<br/>
 Помимо колонок метро и регион, также можно убрать и другие колонки: цену и телефон
	.</p>

