</div>

<p>Начата разработка раздела Аналитики.  <br/>
На данный момент доступен раздел только для Администраторов и включает в себя график поступления заявок по датам, а также круговую диаграмму соотношения свободных и прикрепленных к агентам заявок. <br/>

В дальнейшем планируется создать аналитические графики и диаграммы для агентов (личная статистика) и менеджеров (статистика группы).</p>

